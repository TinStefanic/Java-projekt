    private void diskbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_diskbuttonActionPerformed
        int returnVal = fileChooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            String text = vercnt.getText();
            File file = fileChooser.getSelectedFile();
            try {
                vertexnumber = Integer.parseInt(text);
                BufferedReader br = new BufferedReader(new FileReader(file)); 
                String line;
                int i=0;
                Vector<String> listabridova=new Vector<>();
                for (int k = 0; k<vertexnumber; k++) 
                    for (int j = 0; j<vertexnumber; j++) {
                        if (k==j) continue;
                        listabridova.add(String.valueOf(k)+"-"+String.valueOf(j));
                    }
                graph = new Graph(vertexnumber);
                while ((line = br.readLine()) != null) {
                    String[] odvojeno = line.split(" ");
                    for (int j=0; j<odvojeno.length; j++) graph.addEdge(i, j, Integer.parseInt(odvojeno[j]));
                    i++;
                }
                edgecombo.setModel(new javax.swing.DefaultComboBoxModel<>(listabridova));
                edgecomboActionPerformed(evt);
            } catch (IOException ex) {
                System.out.println("problem accessing file"+file.getAbsolutePath());
            } catch (NumberFormatException nfe) {
                System.out.println("File nontains nonnumbers!");
            }
        } else {
            //korisnik odustao
        }
    }//GEN-LAST:event_diskbuttonActionPerformed

