    private void emptybuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_emptybuttonActionPerformed
        String text = vercnt.getText();
        try {
            vertexnumber=Integer.parseInt(text);
            graph = new Graph(vertexnumber);
            Vector<String> listabridova=new Vector<>();
            if (usmjereniradio.isSelected()) {
                for (int i = 0; i<vertexnumber; i++) 
                    for (int j = 0; j<vertexnumber; j++) {
                        if (i==j) continue;
                        listabridova.add(String.valueOf(i)+"-"+String.valueOf(j));
                    }
            } else {
                for (int i = 0; i<vertexnumber; i++) 
                    for (int j = i+1; j<vertexnumber; j++)
                        listabridova.add(String.valueOf(i)+"-"+String.valueOf(j));
            }
            edgecombo.setModel(new javax.swing.DefaultComboBoxModel<>(listabridova));
            edgecomboActionPerformed(evt);   
        } catch (NumberFormatException nfe) {
            //what if it's not a num?
        }
    }//GEN-LAST:event_emptybuttonActionPerformed

